GRANT SELECT, INSERT, UPDATE, DELETE ON "${tenantSchema}".CLIENT_PROFILES      TO "${appSchema}";
GRANT SELECT, INSERT, UPDATE, DELETE ON "${tenantSchema}".VISIT_RECORDS        TO "${appSchema}";
