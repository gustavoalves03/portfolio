GRANT SELECT, INSERT, UPDATE, DELETE ON "${tenantSchema}".EMPLOYEES            TO "${appSchema}";
GRANT SELECT, INSERT, UPDATE, DELETE ON "${tenantSchema}".EMPLOYEE_CARES       TO "${appSchema}";
GRANT SELECT, INSERT, UPDATE, DELETE ON "${tenantSchema}".LEAVE_REQUESTS       TO "${appSchema}";
GRANT SELECT, INSERT, UPDATE, DELETE ON "${tenantSchema}".EMPLOYEE_DOCUMENTS   TO "${appSchema}";
GRANT SELECT, INSERT, UPDATE, DELETE ON "${tenantSchema}".POSTS                TO "${appSchema}";
GRANT SELECT, INSERT, UPDATE, DELETE ON "${tenantSchema}".POST_IMAGES          TO "${appSchema}";
GRANT SELECT, INSERT, UPDATE, DELETE ON "${tenantSchema}".HOLIDAY_EXCEPTIONS   TO "${appSchema}";
